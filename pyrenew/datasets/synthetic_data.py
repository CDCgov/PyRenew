"""
Loaders for synthetic 126-day California test data.

The synthetic dataset is generated by ``datagen_he_CA_126.py`` and contains
daily infections, hospital admissions, ED visits, and ground-truth parameters
for a single-jurisdiction COVID-19 scenario.
"""

from __future__ import annotations

import json
from importlib.resources import files

import polars as pl


def _synthetic_path() -> files:
    """
    Return the path to the synthetic_CA_126 data directory.

    Returns
    -------
    importlib.resources.abc.Traversable
        Path to the synthetic_CA_126 package directory.
    """
    return files("pyrenew.datasets") / "synthetic_CA_126"


def load_synthetic_true_parameters() -> dict:
    """
    Load ground-truth parameters from the synthetic data generator.

    Returns
    -------
    dict
        True parameter values including R(t) trajectory,
        ascertainment rates, and delay PMFs.
    """
    with open(str(_synthetic_path() / "true_parameters.json")) as f:
        return json.load(f)


def load_synthetic_daily_infections() -> pl.DataFrame:
    """
    Load true daily infections and R(t) from synthetic data.

    Returns
    -------
    pl.DataFrame
        Columns: date, true_infections, true_rt.
    """
    return pl.read_csv(
        source=str(_synthetic_path() / "daily_infections.csv"),
    )


def load_synthetic_daily_hospital_admissions() -> pl.DataFrame:
    """
    Load synthetic daily hospital admissions.

    Returns
    -------
    pl.DataFrame
        Columns: date, geo_value, daily_hosp_admits, pop.
    """
    return pl.read_csv(
        source=str(_synthetic_path() / "daily_hospital_admissions.csv"),
    )


def load_synthetic_daily_ed_visits() -> pl.DataFrame:
    """
    Load synthetic daily ED visits.

    Returns
    -------
    pl.DataFrame
        Columns: date, geo_value, disease, ed_visits.
    """
    return pl.read_csv(
        source=str(_synthetic_path() / "daily_ed_visits.csv"),
    )


def load_synthetic_weekly_hospital_admissions() -> pl.DataFrame:
    """
    Load synthetic weekly hospital admissions.

    Returns
    -------
    pl.DataFrame
        Columns: week_end, weekly_hosp_admits, location, pop.
    """
    return pl.read_csv(
        source=str(_synthetic_path() / "weekly_hospital_admissions.csv"),
    )
